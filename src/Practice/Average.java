package Practice;

public class Average {
    public static void main(String[] args) {
        int M =  80;
        int B = 64;
        int C =  49;
        int C1 = 57;
        int P  = 43;
        int E  = 86;
        int T = (M + B + C + C1 + P + E);
        int A = (T/6);
        System.out.println("Total Marks in 6 Subjects = " +(T));
        System.out.println("Average Marks in 6 Subjects = " +(A));
    }
}
